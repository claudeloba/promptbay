// Generate sidebar assets for L1B3RT4S (jailbreaks) and CL4R1T4S (system prompts)
const fs = require('fs');
const path = require('path');

const projectRoot = process.cwd();
const MAX_FILE_SIZE = 512 * 1024; // 512KB cap per file

function humanize(name) {
    return name
        .replace(/\.[^.]+$/, '')
        .replace(/^[-_#!*]+|[-_]+$/g, '')
        .replace(/[_-]+/g, ' ')
        .replace(/\s+/g, ' ')
        .trim()
        .replace(/\b\w/g, m => m.toUpperCase());
}

function excerpt(text, max) {
    max = max || 200;
    var plain = text.replace(/```[\s\S]*?```/g, ' ').replace(/[#*_~`>\[\]()!]/g, '').replace(/\s+/g, ' ').trim();
    return plain.length > max ? plain.slice(0, max) + '…' : plain;
}

function slugify(value) {
    return String(value || '').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
}

// ── L1B3RT4S ──────────────────────────────────────
function generateLibertas() {
    const dir = path.join(projectRoot, 'L1B3RT4S');
    const out = path.join(projectRoot, 'js', 'libertasAssets.js');
    if (!fs.existsSync(dir)) {
        console.log('L1B3RT4S/ not found — skipping');
        return;
    }

    const entries = fs.readdirSync(dir, { withFileTypes: true })
        .filter(e => e.isFile() && /\.(mkd|txt|json)$/i.test(e.name) && !/^readme\./i.test(e.name))
        .sort((a, b) => a.name.localeCompare(b.name));

    const items = [];
    for (const entry of entries) {
        const filePath = path.join(dir, entry.name);
        const stat = fs.statSync(filePath);
        if (stat.size > MAX_FILE_SIZE) {
            console.log(`  skip ${entry.name} (${(stat.size / 1024 / 1024).toFixed(1)}MB > 512KB cap)`);
            continue;
        }
        const content = fs.readFileSync(filePath, 'utf8');
        items.push({
            id: slugify(entry.name.replace(/\.[^.]+$/, '')),
            title: humanize(entry.name),
            filename: entry.name,
            excerpt: excerpt(content),
            content
        });
    }

    const js = [
        '// Generated by scripts/generate-collection-assets.js',
        '(function () {',
        '    window.libertasAssets = ' + JSON.stringify(items, null, 4) + ';',
        '}());',
        ''
    ].join('\n');
    fs.writeFileSync(out, js, 'utf8');
    console.log(`Wrote ${out} (${items.length} files)`);
}

// ── CL4R1T4S ─────────────────────────────────────
function generateClaritas() {
    const dir = path.join(projectRoot, 'CL4R1T4S');
    const out = path.join(projectRoot, 'js', 'claritasAssets.js');
    if (!fs.existsSync(dir)) {
        console.log('CL4R1T4S/ not found — skipping');
        return;
    }

    const folders = fs.readdirSync(dir, { withFileTypes: true })
        .filter(e => e.isDirectory() && e.name !== '.git')
        .sort((a, b) => a.name.localeCompare(b.name));

    const groups = [];
    for (const folder of folders) {
        const folderPath = path.join(dir, folder.name);
        const files = fs.readdirSync(folderPath, { withFileTypes: true })
            .filter(e => e.isFile() && !/^readme\./i.test(e.name) && !/^license$/i.test(e.name))
            .sort((a, b) => a.name.localeCompare(b.name));

        const items = [];
        for (const file of files) {
            const filePath = path.join(folderPath, file.name);
            const stat = fs.statSync(filePath);
            if (stat.size > MAX_FILE_SIZE) {
                console.log(`  skip ${folder.name}/${file.name} (${(stat.size / 1024 / 1024).toFixed(1)}MB)`);
                continue;
            }
            const content = fs.readFileSync(filePath, 'utf8');
            items.push({
                id: slugify(folder.name + '-' + file.name.replace(/\.[^.]+$/, '')),
                title: humanize(file.name),
                filename: file.name,
                excerpt: excerpt(content),
                content
            });
        }

        if (items.length) {
            groups.push({
                id: slugify(folder.name),
                title: humanize(folder.name),
                files: items
            });
        }
    }

    const js = [
        '// Generated by scripts/generate-collection-assets.js',
        '(function () {',
        '    window.claritasAssets = ' + JSON.stringify(groups, null, 4) + ';',
        '}());',
        ''
    ].join('\n');
    fs.writeFileSync(out, js, 'utf8');
    console.log(`Wrote ${out} (${groups.length} groups)`);
}

generateLibertas();
generateClaritas();
